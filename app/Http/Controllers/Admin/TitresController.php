     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
          'nom' => 'required'
        ]);
        // Créer un quartier
        $titre = new Titre;
        $titre->nom = $request->input('nom');
        $titre->description = $request->input('description');

        // Save Titre
        $titre->save();

        // Redirect
        return redirect('/titres')->with('success', 'Titre créé avec succès');
    }

